a = int(input('a = '))
b = int(input('b = '))
print("%d + %d = %d" % (a, b, a + b))
print("%d - %d = %d" % (a, b, a - b))
print("%d * %d = %d" % (a, b, a * b))
print("%d / %d = %f" % (a, b, a / b))
print("%d // %d = %d" % (a, b, a // b))     # 向上取整
print("%d %% %d = %d" % (a, b, a % b))        # 取余数
print("%d ** %d = %d" % (a, b, a ** b))
